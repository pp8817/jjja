package com.i28minutes.oops.level2.inheritance;

public class Studentwithoutinheritance {

	
		private String name;
		private String email;
		private String phoneNumber;
		private String collegeString;
		private int year;
		
		
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getEmail() {
			return email;
		}
		public void setEmail(String email) {
			this.email = email;
		}
		public String getPhoneNumber() {
			return phoneNumber;
		}
		public void setPhoneNumber(String phoneNumber) {
			this.phoneNumber = phoneNumber;
		}
		public String getCollegeString() {
			return collegeString;
		}
		public void setCollegeString(String collegeString) {
			this.collegeString = collegeString;
		}
		 
		public int getYear() {
			return year;
		}
		public void setYear(int year) {
			this.year = year;
		}
		
		
	

}
